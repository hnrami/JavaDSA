package com.meybise.basic;

//the numbers 1 to 100 without explicitly using any numeric literals 
//in the code is an interesting challenge
public class OneToHundred {

	public static void main(String[] args) {
		
		
		 // Initialize an array with 100 elements
        String[] array = new String[('d')];

        // ASCII values for characters to generate numbers indirectly
        int one = 'a' / 'a'; // 1
 

        for (int i = one; i <= array.length; i++) {
            System.out.println(i);
        }

	}

}
